# Design notes (v3.1):
# - Surrounding chairs (dining chair / office chair / side chair) -> follower, excluded
# - Nightstand / side table / floor lamp / decoration -> follower, excluded
# - Sofa-style seats (lounge armchair / wing chair / club chair / accent chair)
#   are seats that can "stand on their own" and form a zone in a room corner -> allowed as anchor
# - "armchair" used alone usually means accent / lounge -> also allowed as anchor
_CORNER_FOLLOWER_KEYWORDS: Tuple[str, ...] = (
    # surrounding chairs: dragged along by anchors like dining table / desk
    "dining chair", "dining_chair",
    "office chair", "office_chair", "desk chair", "desk_chair",
    "side chair", "side_chair",
    "stool", "bar stool", "counter stool", "bench",
    # bed-adjacent followers
    "nightstand", "night stand", "night_stand",
    # side / corner tables (followers themselves rather than corner anchors)
    "side table", "side_table", "end table", "end_table",
    # decoration / lamps / textiles
    "lamp", "lantern", "sconce",
    "cushion", "pillow", "throw", "blanket",
    "vase", "plant", "planter", "pot ", "pot,", "potted",
    "rug", "carpet", "mat",
    "tray", "decanter", "bottle", "candle", "frame", "art piece",
    "basket", "bookend", "ornament", "sculpture", "figurine",
)

# How close the object hint must be to a boundary to count as "against that side" (between 0 and 0.5)
# 0.30 means x_norm < 0.30 counts as against W, > 0.70 counts as against E (Y similarly).
_CORNER_THRESH: float = 0.30


def _is_corner_follower(label: Optional[str]) -> bool:
    if not label:
        return False
    s = label.lower()
    return any(k in s for k in _CORNER_FOLLOWER_KEYWORDS)


def _classify_corner(x_norm: float, y_norm: float) -> Optional[str]:
    """Decide corner direction from a normalized centroid (image convention: y=0 is N).

    Returns: "NE" / "NW" / "SE" / "SW" or None (not in any corner).
    """
    near_w = x_norm < _CORNER_THRESH
    near_e = x_norm > 1.0 - _CORNER_THRESH
    near_n = y_norm < _CORNER_THRESH
    near_s = y_norm > 1.0 - _CORNER_THRESH
    if near_n and near_e:
        return "NE"
    if near_n and near_w:
        return "NW"
    if near_s and near_e:
        return "SE"
    if near_s and near_w:
        return "SW"
    return None


def _derive_corner_edges(
    skeleton: Dict[str, Any],
    llm_attrs: Dict[str, Dict],
    existing_wall_targets: Set[str],
) -> List[Dict]:
    """Derive in_corner_<X> edges from stage1/LLM-provided geometry_hint.

    Args:
        skeleton: output of _build_node_skeleton.
        llm_attrs: LLM-output node_attributes (with geometry_hint, if any).
        existing_wall_targets: source id set already appearing as against_wall
            in LLM forward edges - those objects skip derivation (avoid double anchoring).

    Returns:
        forward edge list (in_corner_X), in the same shape as other forward edges in
        _merge_skeleton_with_llm; the caller is responsible for adding edge_id and inverse edges.
    """
    corner_by_dir = skeleton.get("corner_by_dir") or {}
    if not corner_by_dir:
        return []

    out: List[Dict] = []
    for n in skeleton["nodes"]:
        # Only floor major objects (no architecture, no minor that has been split off)
        if n.get("node_kind") != "object":
            continue
        if n.get("type") != "Major":
            continue
        if n.get("placement_type") != "floor":
            continue
        oid = n["id"]
        if oid in existing_wall_targets:
            # Already tagged against_wall by the LLM -> do not derive in_corner (semantic conflict)
            continue
        if _is_corner_follower(n.get("label")):
            continue

        # geometry_hint comes from LLM output first (it has visual judgment), falling back to stage1
        # (but stage1 hints are not on skeleton nodes during v3 derivation, so we can only use what the LLM gives us)
        a = (llm_attrs or {}).get(oid) or {}
        gh = a.get("geometry_hint") or {}
        c = gh.get("centroid") if isinstance(gh, dict) else None
        if not isinstance(c, dict):
            continue
        x = c.get("x_norm")
        y = c.get("y_norm")
        if not (isinstance(x, (int, float)) and isinstance(y, (int, float))):
            continue
        corner_dir = _classify_corner(float(x), float(y))
        if not corner_dir:
            continue
        cid = corner_by_dir.get(corner_dir)
        if not cid:
            continue
        rel = f"in_corner_{corner_dir}"
        out.append({
            "source": oid,
            "target": cid,
            "relation": rel,
            "relation_category": "architecture",
            "scope": "object_to_architecture",
            "spatial_signature": {
                "direction_8": corner_dir,
                "overlap": {"type": "none"},
                "alignment": {"x": "none", "y": "none"},
            },
            "visual_reasoning": (
                f"Auto-derived: hint centroid ({x:.2f}, {y:.2f}) sits in "
                f"the {corner_dir} corner region (>= {1-_CORNER_THRESH:.2f} or "
                f"<= {_CORNER_THRESH:.2f} on both axes)."
            ),
            "_derived_corner": True,
        })
    return out


def _make_inverse_edge(edge: Dict) -> Optional[Dict]:
    rel = edge.get("relation")
    if not rel:
        return None
    inv_rel = INVERSE_RELATION_MAP.get(rel)
    if not inv_rel:
        return None

    sig = edge.get("spatial_signature") or {}
    inv_sig = {
        "direction_8": DIRECTION_FLIP.get(sig.get("direction_8", "UNK"), "UNK"),
        "overlap": sig.get("overlap") or {"type": "none"},
        "alignment": {
            "x": ALIGNMENT_FLIP.get((sig.get("alignment") or {}).get("x", "none"), "none"),
            "y": ALIGNMENT_FLIP.get((sig.get("alignment") or {}).get("y", "none"), "none"),
        },
    }
    return {
        "source": edge["target"],
        "target": edge["source"],
        "relation": inv_rel,
        "relation_category": edge.get("relation_category") or _decide_category(inv_rel),
        "scope": edge.get("scope"),
        "spatial_signature": inv_sig,
        "visual_reasoning": f"Auto inverse of {edge['source']} -({rel})-> {edge['target']}.",
        "_auto_generated": True,
    }


