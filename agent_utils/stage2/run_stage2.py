def _build_node_lookup(nodes: List[Dict]) -> Dict[str, Dict]:
    return {n["id"]: n for n in nodes if "id" in n}


def _decide_scope(node_lookup: Dict[str, Dict], sid: str, tid: str) -> str:
    s = node_lookup.get(sid)
    t = node_lookup.get(tid)
    if not s or not t:
        return "intra_zone"
    if s.get("node_kind") == "architecture" or t.get("node_kind") == "architecture":
        return "object_to_architecture"
    if s.get("zone_id") == t.get("zone_id"):
        return "intra_zone"
    return "inter_zone"


def _decide_category(rel: str) -> str:
    pos = {"north_of", "south_of", "east_of", "west_of", "north_east_of",
           "north_west_of", "south_east_of", "south_west_of",
           "overlapping", "partially_overlapping"}
    dist = {"touching", "adjacent_to", "near", "far_from", "separated_by_walkway"}
    align = {"aligned_left", "aligned_right", "aligned_top", "aligned_bottom",
             "aligned_center_x", "aligned_center_y", "in_same_row_as",
             "in_same_column_as", "parallel_to", "perpendicular_to", "centered_on"}
    sup = {"on_top_of", "supports", "mounted_on", "under", "above"}
    cont = {"inside", "contains"}
    func = {"faces_for_use", "used_with", "reachable_from",
            "blocks_access_to", "provides_clearance_for"}
    grp = {"grouped_with", "part_of_set", "surrounds", "surrounded_by", "pairs_with"}
    arch = {"against_wall", "near_wall", "aligned_with_wall",
            "next_to_door", "faces_opening", "along_window",
            "in_corner_NE", "in_corner_NW", "in_corner_SE", "in_corner_SW",
            "contains_corner_NE", "contains_corner_NW",
            "contains_corner_SE", "contains_corner_SW"}

    if rel in pos: return "position"
    if rel in dist: return "distance"
    if rel in align: return "alignment"
    if rel in sup: return "support"
    if rel in cont: return "containment"
    if rel in func: return "functional"
    if rel in grp: return "grouping"
    if rel in arch: return "architecture"
    return "position"


