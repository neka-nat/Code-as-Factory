def show_memory_status():
    memory = Memory(workspace_dir=os.path.dirname(
        os.path.dirname(os.path.abspath(__file__))))
    print("=" * 60)
    print("[>] Memory status")
    print("=" * 60)
    for stage in ["stage1", "stage2"]:
        entry = memory.get_latest(stage=stage, type="result")
        if entry:
            title = entry.metadata.get("title", "untitled")
            from datetime import datetime
            time_str = datetime.fromtimestamp(entry.timestamp).strftime("%m-%d %H:%M")
            print(f"✅ {stage}: {title} ({time_str})")
        else:
            print(f"[X] {stage}: no data")
    print("=" * 60)


def main():
    parser = argparse.ArgumentParser(description="Stage2 - Scene Graph construction (v2)")
    parser.add_argument("--image", "-i", help="Image path")
    parser.add_argument("--output-dir", "-o", default="./output", help="Output directory")
    parser.add_argument("--max-iter", "-n", type=int, default=3, help="Maximum iterations")
    parser.add_argument("--max-tokens", type=int, default=65536, help="LLM max_tokens")
    parser.add_argument("--quiet", "-q", action="store_true", help="Quiet mode")
    parser.add_argument("--status", "-s", action="store_true", help="Show Memory")
    args = parser.parse_args()

    if args.status:
        show_memory_status()
        return 0

    runner = Stage2Runner(
        image_path=args.image, output_dir=args.output_dir,
        max_iterations=args.max_iter, verbose=not args.quiet,
        max_tokens=args.max_tokens,
    )
    success, result, meta = runner.run()
    if result:
        graphs = len(result.get("zone_graphs", []))
        nodes = len(result.get("nodes", []))
        minors = len(result.get("minor_objects", []) or [])
        print(f"\nResult: {'success' if success else 'partial success'}")
        print(f"   Zones: {graphs}, main-graph nodes: {nodes}, "
              f"minor sidecar: {minors}, iterations: {meta['iterations']}")
    return 0 if success else 1


if __name__ == "__main__":
    exit(main())
