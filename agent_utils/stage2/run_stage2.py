# Merge: skeleton + LLM output -> final stage2 JSON
# ============================================================
def _merge_skeleton_with_llm(skeleton: Dict, llm_out: Dict) -> Dict:
    """Combine the code-side skeleton + LLM node_attributes/edges into the final stage2 JSON.

    Output schema (v3): meta / zone_relations / nodes / edges /
    zone_graphs / global_graph / minor_objects

    minor_objects is a sidecar from skeleton["minor_records"], not part of the graph,
    consumed by downstream stage4 (coarse placement) and stage7_small_objects (collision avoidance).
    """
    skel_nodes = skeleton["nodes"]
    parent_hints = skeleton["parent_edges_hint"]
    zone_meta = skeleton["zone_meta"]
    minor_records: List[Dict] = skeleton.get("minor_records", []) or []
    attrs: Dict[str, Dict] = (llm_out or {}).get("node_attributes") or {}

    # 1) Merge nodes: skeleton fields are immutable; only append visual_attributes/geometry_hint
    final_nodes: List[Dict] = []
    for n in skel_nodes:
        a = attrs.get(n["id"]) or {}
        merged = {k: v for k, v in n.items() if k != "parent_id"}
        va = a.get("visual_attributes") or []
        if isinstance(va, list):
            merged["visual_attributes"] = [str(x) for x in va][:8]
        else:
            merged["visual_attributes"] = []
        if isinstance(a.get("geometry_hint"), dict):
            merged["geometry_hint"] = a["geometry_hint"]
        final_nodes.append(merged)

    node_lookup = _build_node_lookup(final_nodes)
    valid_ids = set(node_lookup.keys())

    # 2) Collect forward edges: parent_hints + LLM edges
    forward: List[Dict] = []
    seen_keys: Set[Tuple[str, str, str]] = set()

    for p in parent_hints:
        sid, tid, rel = p["source"], p["target"], p["relation"]
        if sid not in valid_ids or tid not in valid_ids:
            continue
        key = (sid, tid, rel)
        if key in seen_keys:
            continue
        seen_keys.add(key)
        forward.append({
            "source": sid, "target": tid, "relation": rel,
            "relation_category": _decide_category(rel),
            "scope": _decide_scope(node_lookup, sid, tid),
            "spatial_signature": {
                "direction_8": "UNK",
                "overlap": {"type": "full" if rel == "on_top_of" else "none"},
                "alignment": {"x": "center", "y": "center"},
            },
            "visual_reasoning": "Parent relation derived from Stage1 hierarchy.",
            "_from_skeleton": True,
        })

    for e in (llm_out or {}).get("edges", []) or []:
        sid = e.get("source"); tid = e.get("target"); rel = e.get("relation")
        if not (sid and tid and rel):
            continue
        if sid not in valid_ids or tid not in valid_ids:
            continue  # drop edges that reference non-existent ids (validator will still warn)
        if sid == tid:
            continue
        if rel not in INVERSE_RELATION_MAP:
            continue
        key = (sid, tid, rel)
        if key in seen_keys:
            continue
        seen_keys.add(key)
        sig = e.get("spatial_signature") or {}
        forward.append({
            "source": sid, "target": tid, "relation": rel,
            "relation_category": e.get("relation_category") or _decide_category(rel),
            "scope": e.get("scope") or _decide_scope(node_lookup, sid, tid),
            "spatial_signature": {
                "direction_8": sig.get("direction_8", "UNK"),
                "overlap": sig.get("overlap") or {"type": "none"},
                "alignment": sig.get("alignment") or {"x": "none", "y": "none"},
            },
            "visual_reasoning": (e.get("visual_reasoning") or "")[:200],
        })

    # 2.4) v3.2: code-side derivation of against_wall edges (based on hint thresholds)
    #     - Injected after LLM edges, before corner derivation
    #     - This way corner derivation sees against_wall including the derived items
    existing_wall_pairs: Set[Tuple[str, str]] = {
        (fe["source"], fe["target"]) for fe in forward
        if fe.get("relation") == "against_wall"
    }
    derived_wall = _derive_wall_edges(skeleton, attrs, existing_wall_pairs)
    for fe in derived_wall:
        sid, tid, rel = fe["source"], fe["target"], fe["relation"]
        if sid not in valid_ids or tid not in valid_ids:
            continue
        key = (sid, tid, rel)
        if key in seen_keys:
            continue
        seen_keys.add(key)
        forward.append(fe)

    # 2.5) v3.1: code-side derivation of corner anchor edges (in_corner_<NE|NW|SE|SW>)
    #     - Injected after LLM edges and against_wall derivation: skip objects that already have against_wall
    #     - Decided via the LLM's geometry_hint (prioritize the LLM's visual judgment)
    #     - (sid, tid, rel) already tagged in_corner_* by the LLM is in seen_keys, so no duplicates
    against_wall_sources: Set[str] = {
        fe["source"] for fe in forward
        if fe.get("relation") == "against_wall"
    }
    derived_corner = _derive_corner_edges(skeleton, attrs, against_wall_sources)
    for fe in derived_corner:
        sid, tid, rel = fe["source"], fe["target"], fe["relation"]
        if sid not in valid_ids or tid not in valid_ids:
            continue
        key = (sid, tid, rel)
        if key in seen_keys:
            continue
        seen_keys.add(key)
        forward.append(fe)

    # 3) Auto-derive inverse edges
    all_edges: List[Dict] = []
    edge_idx = 1
    for fe in forward:
        eid = f"e_{edge_idx:04d}"
        edge_idx += 1
        all_edges.append({**fe, "edge_id": eid})

        inv = _make_inverse_edge(fe)
        if not inv:
            continue
        inv_key = (inv["source"], inv["target"], inv["relation"])
        if inv_key in seen_keys:
            continue
        seen_keys.add(inv_key)
        inv_eid = f"e_{edge_idx:04d}"
        edge_idx += 1
        all_edges.append({**inv, "edge_id": inv_eid})

    # 4) zone_graphs: group by zone_id (skip global)
    zone_to_nodes: Dict[str, List[str]] = {}
    for n in final_nodes:
        zid = n["zone_id"]
        zone_to_nodes.setdefault(zid, []).append(n["id"])

    zone_graphs: List[Dict] = []
    for zid in sorted(k for k in zone_to_nodes.keys() if k != "global"):
        nids = zone_to_nodes[zid]
        nid_set = set(nids)
        eids = [e["edge_id"] for e in all_edges
                if e["source"] in nid_set and e["target"] in nid_set]
        zone_graphs.append({
            "zone_id": zid,
            "zone_label": zone_meta.get(zid, {}).get("label", zid),
            "zone_function": zone_meta.get(zid, {}).get("function", ""),
            "node_ids": nids,
            "edge_ids": eids,
        })

    # 5) global_graph: all nodes + all edges
    global_graph = {
        "node_ids": [n["id"] for n in final_nodes],
        "edge_ids": [e["edge_id"] for e in all_edges],
    }

    meta = (llm_out or {}).get("meta") or {}
    meta.setdefault("stage", "stage2_object_centric_graph")
    meta.setdefault("north_is", "up")
    meta.setdefault("notes", "Code-injected nodes; auto-generated inverse edges.")
    meta["main_graph_scope"] = "major_and_architecture_only"
    meta["minor_objects_count"] = len(minor_records)

    return {
        "meta": meta,
        "zone_relations": (llm_out or {}).get("zone_relations") or [],
        "nodes": final_nodes,
        "edges": all_edges,
        "zone_graphs": zone_graphs,
        "global_graph": global_graph,
        "minor_objects": minor_records,
    }


# ============================================================
# Stage2Runner
# ============================================================
class Stage2Runner:
    """Stage2 runner - Scene Graph construction (v2)."""

    def __init__(
        self,
        image_path: str = None,
        output_dir: str = "./output",
        max_iterations: int = 3,
        verbose: bool = True,
        model: str = None,
        base_url: str = None,
        api_key: str = None,
        memory_file: str = "agent_memory.jsonl",
        max_tokens: int = 65536,
    ):
        self.image_path = image_path
        self.output_dir = output_dir
        self.max_iterations = max_iterations
        self.verbose = verbose

        self.llm = ChatOpenAI(
            model=model or os.environ.get("SCENEGEN_MODEL") or "gemini-3.5-flash",
            base_url=(
                base_url
                or os.environ.get("SCENEGEN_BASE_URL")
                or os.environ.get("GEMINI_BASE_URL")
                or "https://generativelanguage.googleapis.com/v1beta/openai/"
            ),
            api_key=(
                api_key
                or os.environ.get("SCENEGEN_API_KEY")
                or os.environ.get("GEMINI_API_KEY")
                or os.environ.get("OPENAI_API_KEY")
            ),
            temperature=1,
            max_tokens=max_tokens,
            timeout=600,
            request_timeout=600,
            max_retries=3,
        )

        self.memory = Memory(workspace_dir=parent_dir, memory_file=memory_file)
        self.prompts = PromptManager()
        self.task_prompt = self.prompts.get("Stage2_task")
        self.fix_template = self.prompts.get("Stage2_fix_template")

        self.stage1_json: Optional[Dict] = None
        self.skeleton: Optional[Dict] = None
        self.current_output: Optional[str] = None      # raw LLM string
        self.parsed_llm: Optional[Dict] = None         # parsed LLM JSON
        self.parsed_json: Optional[Dict] = None        # final merged JSON
        self.iteration = 0

    def _log(self, msg: str, level: str = "info"):
        if self.verbose:
            prefix = {"info": "ℹ️", "success": "✅", "warning": "⚠️",
                      "error": "❌", "step": "📋"}.get(level, "")
            print(f"{prefix} {msg}")

    def _encode_image(self, path: str) -> tuple:
        with open(path, "rb") as f:
            b64 = base64.b64encode(f.read()).decode("utf-8")
        ext = os.path.splitext(path)[1].lower()
        mime = {".jpg": "image/jpeg", ".jpeg": "image/jpeg",
                ".png": "image/png"}.get(ext, "image/png")
        return b64, mime

    def _load_stage1(self) -> bool:
        self._log("Fetching Stage1 result from Memory...", "step")
        entry = self.memory.get_latest(stage="stage1", type="result")
        if not entry:
            self._log("No Stage1 result in Memory!", "error")
            return False
        self.stage1_json = entry.content
        if not self.image_path:
            self.image_path = entry.metadata.get("image_path")
        self._log("Stage1 fetched", "success")
        return True

    def _build_skeleton_prompt_payload(self) -> str:
        """Build a compact skeleton for the LLM (save tokens)."""
        nodes = self.skeleton["nodes"]
        rows = []
        for n in nodes:
            row = {
                "id": n["id"],
                "kind": n["node_kind"],
                "zone": n["zone_id"],
                "type": n["type"],
                "placement": n["placement_type"],
                "label": n["label"],
            }
            if n.get("parent_id"):
                row["parent"] = n["parent_id"]
            if n.get("location"):
                row["location"] = n["location"]
            if n.get("arch_subtype"):
                row["arch_subtype"] = n["arch_subtype"]
            rows.append(row)
        return json.dumps(rows, ensure_ascii=False)

    def _generate(self, fix_instructions: str = None) -> Optional[str]:
        self._log(f"Generating JSON (iter {self.iteration}/{self.max_iterations})", "step")

        system_content = self.task_prompt
        if fix_instructions:
            system_content += f"\n\n[Fix requirements]\n{fix_instructions}"

        skeleton_payload = self._build_skeleton_prompt_payload()
        zone_summary = [
            {"zone_id": z["zone_id"], "name": z.get("zone_name", ""),
             "function": z.get("primary_function", "")}
            for z in self.stage1_json.get("decoupled_zones", []) or []
        ]
        scene_dim = (self.stage1_json.get("scene_scale_understanding") or {})

        minor_count = len(self.skeleton.get("minor_records", []) or [])
        user_text = (
            "Stage1 skeleton injected (main graph contains only architecture + major objects, "
            f"{minor_count} minor objects already split off).\n"
            "You only need to output node_attributes and forward edges (for nodes in the skeleton).\n"
            "Do not reference any id in edges that is not in node_skeleton; "
            "minor objects are handled separately by downstream stages and are ignored here.\n\n"
            f"[scene_scale]\n{json.dumps(scene_dim, ensure_ascii=False)}\n\n"
            f"[zones]\n{json.dumps(zone_summary, ensure_ascii=False)}\n\n"
            f"[node_skeleton]\n{skeleton_payload}\n"
        )
        if fix_instructions:
            user_text = f"Re-emit after fixing the following issues:\n{fix_instructions}\n\n" + user_text

        if self.image_path and os.path.exists(self.image_path):
            b64, mime = self._encode_image(self.image_path)
            messages = [
                SystemMessage(content=system_content),
                HumanMessage(content=[
                    {"type": "image_url",
                     "image_url": {"url": f"data:{mime};base64,{b64}"}},
                    {"type": "text", "text": user_text},
                ]),
            ]
        else:
            messages = [SystemMessage(content=system_content),
                        HumanMessage(content=user_text)]

        import time as _time
        max_api_retries = 5
        for attempt in range(1, max_api_retries + 1):
            try:
                response = self.llm.invoke(messages)
                json_str = extract_json_from_response(response.content)
                self.current_output = json_str
                self._log(f"Generation finished ({len(json_str)} chars)", "success")
                return json_str
            except Exception as e:
                err_str = str(e)
                is_retryable = any(k in err_str for k in (
                    "502", "503", "429", "Connection error",
                    "upstream", "timeout", "Timeout"))
                if is_retryable and attempt < max_api_retries:
                    wait = min(10 * attempt, 60)
                    self._log(f"API error (attempt {attempt}/{max_api_retries}): "
                              f"{e} - retrying in {wait}s", "warning")
                    _time.sleep(wait)
                    continue
                self._log(f"Generation failed: {e}", "error")
                import traceback
                traceback.print_exc()
                return None

    def _validate(self) -> ValidationResult:
        self._log("Parsing LLM output + merging skeleton...")

        try:
            self.parsed_llm = json.loads(self.current_output)
        except json.JSONDecodeError as e:
            return ValidationResult(is_valid=False, errors=[f"JSON parse error: {e}"])

        try:
            self.parsed_json = _merge_skeleton_with_llm(self.skeleton, self.parsed_llm)
        except Exception as e:
            return ValidationResult(is_valid=False, errors=[f"Merge failed: {e}"])

        result = validate_stage2_object_centric_schema(
            self.parsed_json, stage1_json=self.stage1_json)

        if result.is_valid:
            self._log(f"Validation passed (warnings={len(result.warnings)})", "success")
        else:
            self._log(f"Validation failed: {len(result.errors)} errors", "error")
        return result

    def _generate_fix_instructions(self, validation_result: ValidationResult) -> str:
        errors_text = "\n".join(f"- {e}" for e in validation_result.errors[:30])
        snippet = (self.current_output[:1500] + "...") \
            if len(self.current_output) > 1500 else self.current_output
        try:
            fix_prompt = self.fix_template.format(
                errors=errors_text, current_json=snippet)
        except Exception:
            fix_prompt = errors_text
        try:
            response = self.llm.invoke([
                SystemMessage(content="You are a JSON Schema repair expert"),
                HumanMessage(content=fix_prompt),
            ])
            return response.content.strip()
        except Exception:
            return errors_text

    def _save_to_memory(self):
        if not self.parsed_json:
            return
        nodes = self.parsed_json.get("nodes", [])
        edges = self.parsed_json.get("edges", [])
        zone_graphs = self.parsed_json.get("zone_graphs", [])
        minors = self.parsed_json.get("minor_objects", []) or []
        title = (f"{len(zone_graphs)} zones, {len(nodes)} main-graph nodes, "
                 f"{len(edges)} relations, {len(minors)} minor")

        # sidecar file path (written uniformly in _save_files; only exposing the path here)
        minor_json_path = os.path.join(self.output_dir, "stage2_minor_objects.json")

        self.memory.add(
            stage="stage2", type="result", content=self.parsed_json,
            metadata={
                "success": True, "title": title,
                "summary": f"{len(zone_graphs)} zones (skeleton-aligned, main graph major-only)",
                "iterations": self.iteration, "image_path": self.image_path,
                "main_graph_scope": "major_and_architecture_only",
                "minor_objects_count": len(minors),
                "minor_objects_json": minor_json_path,
            },
            tags=["stage2", "success"],
        )
        self._log(f"Saved to Memory: {title}", "success")

        # Also store the minor sidecar as a separate Memory entry for easy downstream querying
        if minors:
            self.memory.add(
                stage="stage2", type="minor_objects",
                content={"minor_objects": minors},
                metadata={
                    "title": f"Stage2 minor sidecar ({len(minors)} items)",
                    "minor_objects_count": len(minors),
                    "minor_objects_json": minor_json_path,
                    "image_path": self.image_path,
                },
                tags=["stage2", "minor_objects", "sidecar"],
            )

    def _save_files(self):
        os.makedirs(self.output_dir, exist_ok=True)
        if self.parsed_json:
            path = os.path.join(self.output_dir, "stage2_output.json")
            with open(path, "w", encoding="utf-8") as f:
                json.dump(self.parsed_json, f, ensure_ascii=False, indent=2)
            self._log(f"JSON: {path}")

            # minor sidecar separate file (written even when empty for unified downstream reads)
            minors = self.parsed_json.get("minor_objects", []) or []
            sidecar_path = os.path.join(
                self.output_dir, "stage2_minor_objects.json")
            with open(sidecar_path, "w", encoding="utf-8") as f:
                json.dump({
                    "stage": "stage2",
                    "image_path": self.image_path,
                    "count": len(minors),
                    "minor_objects": minors,
                }, f, ensure_ascii=False, indent=2)
            self._log(f"Minor sidecar: {sidecar_path} ({len(minors)} items)")

        if self.current_output:
            path = os.path.join(self.output_dir, "stage2_raw.txt")
            with open(path, "w", encoding="utf-8") as f:
                f.write(self.current_output)
            self._log(f"Raw: {path}")
        if self.skeleton:
            path = os.path.join(self.output_dir, "stage2_skeleton.json")
            with open(path, "w", encoding="utf-8") as f:
                json.dump(self.skeleton, f, ensure_ascii=False, indent=2)
            self._log(f"Skeleton: {path}")

    def run(self) -> tuple:
        print("\n" + "=" * 60)
        print("Stage2 - Scene Graph construction)")
        print("=" * 60)

        if not self._load_stage1():
            return False, None, {}

        # Build skeleton (code is authoritative)
        try:
            self.skeleton = _build_node_skeleton(self.stage1_json)
        except Exception as e:
            self._log(f"Skeleton build failed: {e}", "error")
            return False, None, {}
        self._log(
            f"Skeleton: {len(self.skeleton['nodes'])} main-graph nodes (major+arch), "
            f"{len(self.skeleton['parent_edges_hint'])} parent-child edges, "
            f"{len(self.skeleton.get('minor_records', []) or [])} minor (sidecar)",
            "success",
        )

        if self.image_path:
            self._log(f"Image: {self.image_path}")

        fix_instructions = None
        for self.iteration in range(1, self.max_iterations + 1):
            print(f"\n{'─' * 40}")
            self._log(f"Iteration {self.iteration}/{self.max_iterations}", "step")
            print(f"{'─' * 40}")

            output = self._generate(fix_instructions)
            if not output:
                continue

            validation = self._validate()
            if validation.is_valid:
                self._save_to_memory()
                self._save_files()
                print("\n" + "=" * 60)
                print("[OK] Stage2 done!")
                print("=" * 60)
                return True, self.parsed_json, {"iterations": self.iteration}

            if self.iteration < self.max_iterations:
                fix_instructions = self._generate_fix_instructions(validation)

        self._log("Reached maximum iterations", "warning")
        self._save_files()
        return False, self.parsed_json, {"iterations": self.iteration}


