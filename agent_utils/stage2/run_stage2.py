def _normalize_location(loc: Optional[str]) -> str:
    if not loc:
        return ""
    raw = loc.strip().lower()
    key = raw.replace(" ", "_")
    direct = LOCATION_NORM.get(key)
    if direct:
        return direct
    # short codes like "n", "ne", "sw"
    if len(key) <= 3:
        return key.upper()
    # v3.2 fallback: search a long description for orientation keywords (e.g. "west boundary wall",
    # "north wall, eastern half") - prefer compound directions (NE/NW/SE/SW), then single directions.
    for compound, code in [
        ("north_east", "NE"), ("northeast", "NE"),
        ("north_west", "NW"), ("northwest", "NW"),
        ("south_east", "SE"), ("southeast", "SE"),
        ("south_west", "SW"), ("southwest", "SW"),
    ]:
        if compound in key:
            return code
    for single, code in [
        ("north", "N"), ("south", "S"), ("east", "E"), ("west", "W"),
    ]:
        if single in key:
            return code
    # v3.2 fallback for image-orientation phrasing ("top of image",
    # "bottom of image", "right of image", "left of image"). North = top
    # by image convention.
    for phrase, code in [
        ("top", "N"), ("bottom", "S"), ("right", "E"), ("left", "W"),
    ]:
        if phrase in key:
            return code
    return ""


def _build_node_skeleton(stage1_json: Dict) -> Dict[str, Any]:
    """Derive node skeleton from stage1 (v3.1: main graph contains major + architecture + corner anchors).

    Minor objects do not enter the main graph; they fall into the minor_records sidecar.

    Returns:
        {
            "nodes": [...],             # main-graph nodes (architecture + corner + major)
            "parent_edges_hint": [...], # main-graph support/mount edges (both ends in main graph)
            "arch_by_location": {...},  # "wall:north" -> "feat_001"
            "corner_by_dir": {...},     # "NE"/"NW"/"SE"/"SW" -> "corner_NE"/...
            "zone_meta": {...},         # zone_id -> {label, function}
            "minor_records": [...],     # sidecar: all minor objects + parent info
        }
    """
    nodes: List[Dict] = []
    arch_by_location: Dict[str, str] = {}
    zone_meta: Dict[str, Dict[str, str]] = {}

    # v3.1: 4 virtual corner anchor nodes (always injected, even when no objects derive in_corner_X;
    # stage3 / stage6_geometry must recognize is_virtual=True and skip geometry creation)
    corner_by_dir: Dict[str, str] = {}
    for dir_short, label_full in [
        ("NE", "Northeast corner anchor (virtual)"),
        ("NW", "Northwest corner anchor (virtual)"),
        ("SE", "Southeast corner anchor (virtual)"),
        ("SW", "Southwest corner anchor (virtual)"),
    ]:
        cid = f"corner_{dir_short}"
        corner_by_dir[dir_short] = cid
        nodes.append({
            "id": cid,
            "node_kind": "architecture",
            "label": label_full,
            "zone_id": "global",
            "type": "Fixed",
            "placement_type": "floor",
            "arch_subtype": "corner",
            "location": dir_short,
            "is_virtual": True,
        })

    # 1) architectural_features -> architecture nodes (reuse feat_xxx id)
    for feat in stage1_json.get("architectural_features", []) or []:
        fid = feat.get("feature_id")
        if not fid:
            continue
        ftype = (feat.get("feature_type") or "unknown").lower()
        loc_raw = feat.get("location") or ""
        loc_norm = _normalize_location(loc_raw)

        placement = "wall" if ftype in {"wall", "door", "window"} else "floor"
        nodes.append({
            "id": fid,
            "node_kind": "architecture",
            "label": (feat.get("description") or fid)[:120],
            "zone_id": "global",  # arch features do not belong to a specific zone
            "type": "Fixed",
            "placement_type": placement,
            "arch_subtype": ftype,
            "location": loc_norm,
        })
        if ftype == "wall" and loc_norm:
            # Support both "wall:north" and "wall:n" stage1 spellings
            arch_by_location[f"wall:{loc_raw.strip().lower()}"] = fid
            arch_by_location[f"wall:{loc_norm.lower()}"] = fid

    # 2) First pass: collect metadata for all stage1 objects (used in the
    #    minor sidecar to append parent_label / parent_placement_type fields
    #    for downstream consumers)
    obj_meta: Dict[str, Dict[str, Any]] = {}
    for zone in stage1_json.get("decoupled_zones", []) or []:
        zid = zone.get("zone_id")
        for obj in zone.get("object_hierarchy", []) or []:
            oid = obj.get("object_id")
            if not oid:
                continue
            obj_meta[oid] = {
                "name": obj.get("name", oid),
                "zone_id": zid,
                "placement_type": obj.get("placement_type") or "",
                "category": (obj.get("category") or "minor").lower(),
            }

    def _resolve_parent_id(parent_raw: Optional[str]) -> Optional[str]:
        """Normalize parent_object: obj_xxx / feat_xxx / wall:east / None."""
        if not isinstance(parent_raw, str) or not parent_raw:
            return None
        if parent_raw.startswith("obj_") or parent_raw.startswith("feat_"):
            return parent_raw
        if parent_raw.lower().startswith("wall:"):
            return arch_by_location.get(parent_raw.lower())
        return None

    # 3) Second pass: walk zones + object_hierarchy
    #    - major: enters main-graph nodes + parent_edges_hint
    #    - minor: only enters the minor_records sidecar, not the main graph
    parent_hints: List[Dict] = []
    minor_records: List[Dict] = []
    seen_obj_ids: Set[str] = set()
    for zone in stage1_json.get("decoupled_zones", []) or []:
        zid = zone.get("zone_id")
        if not zid:
            continue
        zone_meta[zid] = {
            "label": zone.get("zone_name", zid),
            "function": zone.get("primary_function", ""),
        }
        for obj in zone.get("object_hierarchy", []) or []:
            oid = obj.get("object_id")
            if not oid or oid in seen_obj_ids:
                continue
            seen_obj_ids.add(oid)

            cat = (obj.get("category") or "minor").lower()
            placement = obj.get("placement_type") or "floor"
            parent_id = _resolve_parent_id(obj.get("parent_object"))
            label = obj.get("name", oid)

            if cat != "major":
                # ---- minor goes to sidecar ----
                pmeta = obj_meta.get(parent_id) if parent_id else None
                minor_records.append({
                    "id": oid,
                    "label": label,
                    "zone_id": zid,
                    "placement_type": placement,
                    "category": cat,
                    "parent_id": parent_id,
                    "parent_label": (pmeta or {}).get("name") if pmeta else None,
                    "parent_placement_type": (
                        (pmeta or {}).get("placement_type") if pmeta else None
                    ),
                    "parent_category": (
                        (pmeta or {}).get("category") if pmeta else None
                    ),
                    # When parent is not in obj_meta but resolvable via arch_by_location, mark it
                    "parent_kind": (
                        "architecture"
                        if (parent_id and parent_id.startswith("feat_"))
                        else ("object" if parent_id else None)
                    ),
                })
                continue

            # ---- major: enters main graph ----
            nodes.append({
                "id": oid,
                "node_kind": "object",
                "label": label,
                "zone_id": zid,
                "type": "Major",
                "placement_type": placement,
                "parent_id": parent_id,  # internal-only, removed during merge
            })

            # Derive parent->child support / mount edges (both ends must be in the main graph)
            if not parent_id:
                continue
            parent_in_main = (
                parent_id.startswith("feat_")
                or (obj_meta.get(parent_id, {}).get("category") == "major")
            )
            if not parent_in_main:
                continue
            if placement == "wall":
                rel = "mounted_on"
            elif placement == "surface":
                rel = "on_top_of"
            else:
                rel = None
            if rel:
                parent_hints.append({
                    "source": oid, "target": parent_id, "relation": rel,
                })

    return {
        "nodes": nodes,
        "parent_edges_hint": parent_hints,
        "arch_by_location": arch_by_location,
        "corner_by_dir": corner_by_dir,
        "zone_meta": zone_meta,
        "minor_records": minor_records,
    }


