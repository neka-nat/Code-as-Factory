# v3.2: against_wall derivation (code side, no LLM dependency)
# ============================================================
# The stage2 LLM frequently misses against_wall labels (especially when objects are not very close to the image edge).
# Here, based on the LLM-provided hint.centroid, when the norm distance to any boundary is < THRESH,
# derive against_wall to the wall feat in that direction. Derivation order is before corner,
# so corner derivation can correctly "skip objects that already have against_wall".
#
# Note: hint is the object center, not its edge, so the threshold is a bit lenient (covers the LLM's
# systematic 0.05~0.10 norm inward bias). Also only derive for placement_type=floor Major objects;
# do not derive for surface / wall objects (those are handled by parent edges).
_WALL_HINT_THRESH: float = 0.22  # norm distance: center within 0.22 of the wall counts as against the wall

# "Non-flush" blacklist: even if hint is near a wall, do not derive against_wall for these types.
# (chairs around tables, dining/coffee tables in the room center, lamps/plants placed freely as decoration)
# Note: nightstand / dresser / bookcase / sideboard etc. are NOT in this list -
# they should be allowed to derive against_wall.
_WALL_NONFLUSH_KEYWORDS: Tuple[str, ...] = (
    # exclude all chairs (lounge / armchair are also free-standing, not against the wall)
    "chair", "armchair", "stool", "ottoman", "bench", "seat",
    # tables (except desk, which is still an against-wall candidate - desk is not in this list)
    "dining table", "dining_table",
    "coffee table", "coffee_table",
    "side table", "side_table", "end table", "end_table",
    "round table", "round_table",
    # decoration
    "lamp", "lantern", "plant", "planter", "pot", "potted",
    "rug", "carpet", "mat",
)


def _is_wall_nonflush(label: Optional[str]) -> bool:
    if not label:
        return False
    s = label.lower()
    return any(k in s for k in _WALL_NONFLUSH_KEYWORDS)


def _derive_wall_edges(
    skeleton: Dict[str, Any],
    llm_attrs: Dict[str, Dict],
    existing_wall_pairs: Set[Tuple[str, str]],
) -> List[Dict]:
    """Derive against_wall edges to the wall feat in the corresponding direction based on hint.centroid.

    Args:
        skeleton: output of _build_node_skeleton.
        llm_attrs: LLM-output node_attributes (with geometry_hint).
        existing_wall_pairs: against_wall (sid, tid) pairs already present in
            LLM forward edges, used for dedup.

    Returns:
        forward edge list (against_wall); the caller is responsible for adding edge_id and inverse edges.
    """
    arch_by_loc = skeleton.get("arch_by_location") or {}
    if not arch_by_loc:
        return []

    # Index: short direction -> wall feat id (fallback)
    # In _build_node_skeleton, location is already normalized to N/S/E/W via _normalize_location,
    # but arch_by_location keys use lowercase ("wall:n" etc.), so look up with lowercase here;
    # also try long form ("wall:north" etc.) as a fallback.
    dir_to_wall: Dict[str, str] = {}
    long_form = {"N": "north", "S": "south", "E": "east", "W": "west"}
    for d in ("N", "S", "E", "W"):
        fid = (arch_by_loc.get(f"wall:{d.lower()}") or
               arch_by_loc.get(f"wall:{long_form[d]}"))
        if fid:
            dir_to_wall[d] = fid

    if not dir_to_wall:
        return []

    out: List[Dict] = []
    for n in skeleton["nodes"]:
        if n.get("node_kind") != "object":
            continue
        if n.get("type") != "Major":
            continue
        if n.get("placement_type") != "floor":
            continue
        if _is_wall_nonflush(n.get("label")):
            continue
        oid = n["id"]

        a = (llm_attrs or {}).get(oid) or {}
        gh = a.get("geometry_hint") or {}
        c = gh.get("centroid") if isinstance(gh, dict) else None
        if not isinstance(c, dict):
            continue
        x = c.get("x_norm")
        y = c.get("y_norm")
        if not (isinstance(x, (int, float)) and isinstance(y, (int, float))):
            continue
        x = float(x); y = float(y)

        # Distance to the 4 walls (image convention: y=0 is N at top)
        dists = {
            "W": x,
            "E": 1.0 - x,
            "N": y,
            "S": 1.0 - y,
        }
        # Pick the closest one (and < THRESH)
        best_dir, best_d = min(dists.items(), key=lambda kv: kv[1])
        if best_d > _WALL_HINT_THRESH:
            continue
        wall_id = dir_to_wall.get(best_dir)
        if not wall_id:
            continue
        if (oid, wall_id) in existing_wall_pairs:
            continue  # already produced by the LLM, do not duplicate

        out.append({
            "source": oid,
            "target": wall_id,
            "relation": "against_wall",
            "relation_category": "architecture",
            "scope": "object_to_architecture",
            "spatial_signature": {
                "direction_8": best_dir,
                "overlap": {"type": "none"},
                "alignment": {"x": "none", "y": "none"},
            },
            "visual_reasoning": (
                f"Auto-derived: hint centroid ({x:.2f}, {y:.2f}) sits "
                f"{best_d:.2f} norm from the {best_dir} wall (< "
                f"{_WALL_HINT_THRESH:.2f}); flush against {wall_id}."
            ),
            "_derived_wall": True,
        })
    return out


