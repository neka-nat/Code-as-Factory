    # ---------- Balanced Parenthesis Extraction ----------
    
    @staticmethod
    def _extract_balanced_paren(text: str, start: int) -> Optional[str]:
        """Extract balanced parenthesized expression starting at text[start]='('."""
        if start >= len(text) or text[start] != '(':
            return None
        depth = 0
        for i in range(start, len(text)):
            if text[i] == '(':
                depth += 1
            elif text[i] == ')':
                depth -= 1
                if depth == 0:
                    return text[start:i + 1]
        return None
    
    @staticmethod
    def _strip_trailing_comment_outside_strings(line: str) -> str:
        """Remove a Python `# ...` comment from one source line, only outside quotes.

        Stage 3/4 often appends inline comments after tuple closings, e.g.
        `(bench_cx, bench_cy, 0.45),  # H_work / 2`.  `_collect_full_call` joins
        continuation lines with spaces; if the `#` is kept, everything after it on
        that merged line is treated as comment and the next argument (dimensions)
        is lost — dimensions become (0,0,0) and Stage 8 geometry hallucinates.
        """
        in_str = False
        quote_ch = None
        escape = False
        i = 0
        while i < len(line):
            ch = line[i]
            if escape:
                escape = False
                i += 1
                continue
            if ch == "\\" and in_str:
                escape = True
                i += 1
                continue
            if not in_str:
                if ch in ('"', "'"):
                    in_str = True
                    quote_ch = ch
                elif ch == "#":
                    return line[:i].rstrip()
            else:
                if ch == quote_ch:
                    in_str = False
                    quote_ch = None
            i += 1
        return line

    @staticmethod
    def _split_top_level_args(s: str) -> List[str]:
        """Split by commas at top level only (not inside parens/brackets)."""
        args = []
        depth_paren = 0
        depth_bracket = 0
        current = []
        for ch in s:
            if ch == '(':
                depth_paren += 1
            elif ch == ')':
                depth_paren -= 1
            elif ch == '[':
                depth_bracket += 1
            elif ch == ']':
                depth_bracket -= 1
            elif ch == ',' and depth_paren == 0 and depth_bracket == 0:
                args.append(''.join(current).strip())
                current = []
                continue
            current.append(ch)
        if current:
            args.append(''.join(current).strip())
        return args
    
