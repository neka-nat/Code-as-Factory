    def _extract_string_value(self, s: str) -> Optional[str]:
        """Extract string value from an argument like '"Name"' or 'f"Name"'.

        Returns ``None`` for f-strings that contain ``{…}`` placeholders —
        those are runtime-templated names and must not be stored verbatim
        (otherwise a literal ``{idx}`` shows up in object names downstream).
        """
        s = s.strip()
        # Direct string: "Name" or 'Name'
        m = re.match(r'^["\']([^"\']+)["\']$', s)
        if m:
            return m.group(1)
        # Plain f-string with no placeholder: f"Name" / f'Name'
        m = re.match(r'^[fF]["\']([^"\'{}\\]+)["\']$', s)
        if m:
            return m.group(1)
        return None
    
    def _safe_eval_tuple(self, s: str) -> Tuple[float, float, float]:
        """Safely evaluate a tuple expression, handling nested function calls."""
        import math
        s = s.strip()

        # Try evaluating the whole expression first (handles variable refs like tv_console_dim)
        try:
            val = eval(s, {"__builtins__": {}}, self.eval_env)
            if isinstance(val, (tuple, list)) and len(val) >= 2:
                return tuple(float(v) for v in val[:3]) + ((0.0,) * max(0, 3 - len(val)))
        except:
            pass

        # Remove outer parens if present
        if s.startswith('(') and s.endswith(')'):
            inner = s[1:-1]
        else:
            inner = s

        # Split at top-level commas
        parts = self._split_top_level_args(inner)

        values = []
        for part in parts:
            part = part.strip()
            try:
                values.append(float(part))
            except ValueError:
                try:
                    val = eval(part, {"__builtins__": {}}, self.eval_env)
                    values.append(float(val))
                except:
                    values.append(0.0)
        
        while len(values) < 3:
            values.append(0.0)
        return tuple(values[:3])
    
    def _is_architectural(self, name: str) -> bool:
        """Check if this is an architectural element (wall, floor, etc.).

        IMPORTANT: We must NOT drop wall-mounted decorations / furniture whose
        names happen to contain an architectural word. For example:
          - "Wall_Art_1", "South_Wall_Art", "Wall_Clock", "Wall_Mirror"
          - "Floor_Lamp", "Floor_Vase"
          - "Window_Bench", "Door_Mat"
        These are *objects placed on* an architectural surface, not the
        surface itself. Previously this method used naive substring matching,
        which:
          1) matched "art" inside "partition_1", wrongly dropping walls, and
          2) matched "wall" inside "Wall_Art_1", wrongly dropping wall art.
        We now use word-boundary (token) matching and let decor/furniture
        tokens override architectural tokens.
        """
        import re
        name_lower = name.lower()

        def has_token(token: str) -> bool:
            # Match `token` as a whole word, where the separator can be any
            # non-letter character (underscore, dash, space, digit) or the
            # start/end of the string. This avoids "art" matching "partition"
            # or "wall" matching "drywall_trim".
            return bool(re.search(rf'(?:^|[^a-z]){re.escape(token)}(?:$|[^a-z])', name_lower))

        # Tokens that identify a piece of furniture or wall/ceiling decor.
        # If ANY of these appear as a whole token, the object is a decoration
        # or furniture piece placed on an architectural surface — NOT the
        # surface itself.
        decor_or_furniture_tokens = (
            # Wall art / pictures / mirrors / clocks
            'art', 'artwork', 'decor', 'decoration', 'poster', 'picture',
            'painting', 'mirror', 'clock', 'photo', 'photograph', 'print',
            'canvas', 'tapestry', 'mural',
            # Pin / memo / chalk / white / peg / bulletin boards
            'corkboard', 'pinboard', 'bulletin', 'chalkboard', 'whiteboard',
            'pegboard', 'noticeboard', 'memoboard', 'memo',
            # Soft decor
            'wreath', 'garland', 'macrame', 'dreamcatcher', 'pennant',
            'banner', 'flag', 'plaque', 'monogram',
            # Shelves / hooks / racks mounted on wall
            'shelf', 'shelves', 'bookshelf', 'bookshelves',
            'hook', 'rack', 'bracket', 'sconce', 'hanger',
            # Lighting / plants / vases on surfaces
            'lamp', 'light', 'lantern', 'chandelier',
            'vase', 'plant', 'planter', 'pot',
            # Seating / tables / storage (floor-placed furniture)
            'bench', 'stool', 'chair', 'sofa', 'couch', 'armchair', 'ottoman',
            'table', 'desk', 'nightstand', 'dresser', 'cabinet', 'wardrobe',
            'bookcase', 'sideboard', 'console', 'shelving',
            'bed', 'headboard', 'footboard', 'mattress',
            # Floor coverings and misc
            'mat', 'rug', 'carpet', 'runner',
            # Doormat, doorstop, etc. (still decor / utility)
            'doormat', 'doorstop',
            # Kids / storage / toys
            'toy', 'bin', 'basket', 'crate', 'box', 'hamper',
        )
        if any(has_token(t) for t in decor_or_furniture_tokens):
            return False

        # Tokens that mark a genuine architectural structure.
        architectural_tokens = (
            'floor', 'wall', 'ceiling', 'partition', 'door', 'window',
            'curtain', 'blinds', 'shutter', 'entrance', 'baseboard',
            'skirting', 'molding', 'moulding', 'cornice', 'beam',
            'column', 'pillar', 'arch', 'archway', 'threshold',
        )
        return any(has_token(t) for t in architectural_tokens)


