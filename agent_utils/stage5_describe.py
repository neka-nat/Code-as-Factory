    def _extract_scene_dim(self, var_name: str, default: float) -> float:
        """Extract a scalar like SCENE_W / SCENE_D from arbitrary assignment
        syntax in the parsed Blender code.

        Supports:
          * `SCENE_W = 6.5`
          * `SCENE_W: float = 6.5`
          * `SCENE_W, SCENE_D = 6.5, 5.5`           (tuple unpacking, common)
          * `SCENE_D, SCENE_W = 5.5, 6.5`           (any order)
          * `SCENE_W, SCENE_D, WALL_H = 6.5, 5.5, 2.7`

        Returns `default` when no recognizable assignment is found. We
        deliberately scan ALL matches and pick the FIRST plausible one (so
        that a later in-function re-assignment does not silently override
        the canonical value at the top of run_layout_engine).
        """
        # IMPORTANT: order matters. Tuple-unpacking MUST be checked first,
        # otherwise `SCENE_W, SCENE_D = 6.5, 5.5` would (wrongly) let a
        # naive single-var regex match `SCENE_D = 6.5` and assign 6.5 to
        # SCENE_D as well.

        # Pattern 1: tuple unpacking — find any line whose LHS is a
        # comma-separated list of identifiers and whose RHS is a comma-
        # separated list of numeric literals; then index by the position
        # of `var_name` on the LHS.
        tuple_pat = re.compile(
            r'^[ \t]*([A-Za-z_]\w*(?:\s*,\s*[A-Za-z_]\w*)+)\s*=\s*'
            r'((?:-?\d+(?:\.\d+)?)(?:\s*,\s*-?\d+(?:\.\d+)?)+)\s*$',
            re.MULTILINE,
        )
        for tm in tuple_pat.finditer(self.code):
            lhs_names = [n.strip() for n in tm.group(1).split(',')]
            rhs_vals = [v.strip() for v in tm.group(2).split(',')]
            if var_name in lhs_names and len(lhs_names) == len(rhs_vals):
                idx = lhs_names.index(var_name)
                try:
                    return float(rhs_vals[idx])
                except (TypeError, ValueError):
                    continue

        # Pattern 2: simple `SCENE_W = 6.5` (optionally with type hint),
        # but ONLY when `var_name` is the sole identifier on the LHS. We
        # enforce this by anchoring the match to the start of a line and
        # disallowing any other identifier between line-start and the
        # `=` sign.
        single_pat = re.compile(
            rf'^[ \t]*{re.escape(var_name)}\s*(?::\s*[A-Za-z_][\w\[\], ]*)?\s*=\s*'
            r'(-?\d+(?:\.\d+)?)\s*(?:#.*)?$',
            re.MULTILINE,
        )
        m = single_pat.search(self.code)
        if m:
            try:
                return float(m.group(1))
            except (TypeError, ValueError):
                pass

        return float(default)

    def _build_eval_env(self):
        """Build evaluation environment from code: SCENE_W, SCENE_D, to_world, etc."""
        import math

        # Extract SCENE_W / SCENE_D from code. We MUST handle three syntaxes
        # observed in Stage 3 / Stage 4 outputs, in order of preference:
        #
        #   1) Tuple unpacking (most common in current pipeline):
        #          SCENE_W, SCENE_D = 6.5, 5.5
        #   2) Single assignment:
        #          SCENE_W = 6.5
        #          SCENE_D = 5.5
        #   3) Reverse-order tuple unpacking (defensive):
        #          SCENE_D, SCENE_W = 5.5, 6.5
        #
        # The previous regex `SCENE_W\s*[=,]\s*([\d.]+)` SILENTLY failed on
        # form (1) — the comma matched fine but the next token "SCENE_D" is
        # not a digit, so the whole regex didn't match. The code then
        # fell back to the hard-coded default SCENE_W=8.5, which is how a
        # Stage 4 expression like `-SCENE_W/2 + 0.05` ended up evaluating to
        # -4.2 instead of the correct -3.2 for a 6.5 m wide kitchen.
        scene_w = self._extract_scene_dim('SCENE_W', default=8.5)
        scene_d = self._extract_scene_dim('SCENE_D', default=7.0)
        self.eval_env['SCENE_W'] = scene_w
        self.eval_env['SCENE_D'] = scene_d

        SW = self.eval_env['SCENE_W']
        SD = self.eval_env['SCENE_D']
        
        # Build to_world helper if present in code
        def to_world(x_norm, y_norm):
            return ((x_norm - 0.5) * SW, (0.5 - y_norm) * SD)
        
        self.eval_env['to_world'] = to_world
        self.eval_env['math'] = math
        self.eval_env['pi'] = math.pi
        self.eval_env['WALL_H'] = 2.8
        self.eval_env['WALL_T'] = 0.15

        # mathutils exists only inside Blender; Stage 3/4 lab code uses
        # mathutils.Vector / mathutils.Euler for island bench offsets.  A tiny
        # stub is enough for the parser eval passes (Z-yaw benches are common).
        class _Vec:
            __slots__ = ("x", "y", "z")

            def __init__(self, xyz):
                t = tuple(xyz)
                if len(t) >= 3:
                    self.x, self.y, self.z = float(t[0]), float(t[1]), float(t[2])
                else:
                    self.x = self.y = self.z = 0.0

        class _Mat3:
            __slots__ = ("m",)

            def __init__(self, rows):
                self.m = rows

            def __matmul__(self, other):
                if isinstance(other, _Vec):
                    m = self.m
                    x = m[0][0] * other.x + m[0][1] * other.y + m[0][2] * other.z
                    y = m[1][0] * other.x + m[1][1] * other.y + m[1][2] * other.z
                    z = m[2][0] * other.x + m[2][1] * other.y + m[2][2] * other.z
                    return _Vec((x, y, z))
                return NotImplemented

        class _Euler:
            def __init__(self, angles, order="XYZ"):
                self.angles = tuple(float(a) for a in angles)
                self.order = order

            def to_matrix(self):
                rx, ry, rz = self.angles[0], self.angles[1], self.angles[2]
                if abs(rx) < 1e-9 and abs(ry) < 1e-9:
                    c, s = math.cos(rz), math.sin(rz)
                    return _Mat3(
                        [[c, -s, 0.0], [s, c, 0.0], [0.0, 0.0, 1.0]]
                    )
                cx, sx = math.cos(rx), math.sin(rx)
                cy, sy = math.cos(ry), math.sin(ry)
                cz, sz = math.cos(rz), math.sin(rz)
                rx_m = [[1, 0, 0], [0, cx, -sx], [0, sx, cx]]
                ry_m = [[cy, 0, sy], [0, 1, 0], [-sy, 0, cy]]
                rz_m = [[cz, -sz, 0], [sz, cz, 0], [0, 0, 1]]

                def mm(a, b):
                    return [
                        [
                            sum(a[i][k] * b[k][j] for k in range(3))
                            for j in range(3)
                        ]
                        for i in range(3)
                    ]

                return _Mat3(mm(rz_m, mm(ry_m, rx_m)))

        class _Mathutils:
            Vector = staticmethod(lambda t: _Vec(t))
            Euler = _Euler

        self.eval_env["mathutils"] = _Mathutils()
        
        # Extract variable assignments like loc_desk = to_world(0.38, 0.62)
        var_pattern = r'(\w+)\s*=\s*to_world\s*\(\s*([\d.]+)\s*,\s*([\d.]+)\s*\)'
        for m in re.finditer(var_pattern, self.code):
            vname = m.group(1)
            x_n, y_n = float(m.group(2)), float(m.group(3))
            self.eval_env[vname] = to_world(x_n, y_n)
        
        # Tuple variable assignments are resolved inside the multi-pass loop below
        
        # Extract scalar and multi-variable assignments with multi-pass
        # resolution to handle dependency chains like:
        #   bed_d = 2.1
        #   bed_y = SCENE_D/2 - bed_d/2   (depends on bed_d and SCENE_D)
        safe_builtins = {"abs": abs, "min": min, "max": max, "round": round, 
                         "int": int, "float": float, "len": len}
        
        scalar_pattern = r'^ {4}(\w+)\s*=\s*([^=\n][^\n]*?)\s*$'
        multi_assign_pattern = r'^ {4}(\w+(?:\s*,\s*\w+)+)\s*=\s*([^=\n][^\n]*?)\s*$'
        
        # Collect all candidate assignments first (preserving code order)
        scalar_candidates = []
        for m in re.finditer(scalar_pattern, self.code, re.MULTILINE):
            vname = m.group(1)
            expr = BlenderCodeParser._strip_trailing_comment_outside_strings(
                m.group(2).strip()
            ).strip()
            if expr.startswith(('create_', 'bpy.', '"', "'", '[', '{')):
                continue
            if '=' in expr and not any(op in expr for op in ['==', '!=', '<=', '>=']):
                continue
            scalar_candidates.append((vname, expr))
        
        multi_candidates = []
        for m in re.finditer(multi_assign_pattern, self.code, re.MULTILINE):
            names = [n.strip() for n in m.group(1).split(',')]
            vals_str = BlenderCodeParser._strip_trailing_comment_outside_strings(
                m.group(2).strip()
            ).strip()
            multi_candidates.append((names, vals_str))
        
        tuple_candidates = []
        tuple_var_pattern = r'(\w+)\s*=\s*\(([^=][^;]*?)\)\s*$'
        for m in re.finditer(tuple_var_pattern, self.code, re.MULTILINE):
            inner = BlenderCodeParser._strip_trailing_comment_outside_strings(
                m.group(2).strip()
            ).strip()
            tuple_candidates.append((m.group(1), inner))
        
        MAX_PASSES = 5
        for _ in range(MAX_PASSES):
            resolved_any = False
            
            for vname, expr in scalar_candidates:
                if vname in self.eval_env:
                    continue
                try:
                    val = eval(expr, {"__builtins__": safe_builtins}, self.eval_env)
                    self.eval_env[vname] = val
                    resolved_any = True
                except:
                    pass
            
            for names, vals_str in multi_candidates:
                if any(n in self.eval_env for n in names):
                    continue
                try:
                    vals = eval(f'({vals_str},)' if ',' not in vals_str else f'({vals_str})',
                               {"__builtins__": safe_builtins}, self.eval_env)
                    if len(vals) == len(names):
                        for n, v in zip(names, vals):
                            self.eval_env[n] = v
                        resolved_any = True
                except:
                    pass
            
            for vname, inner_expr in tuple_candidates:
                if vname in self.eval_env:
                    continue
                try:
                    val = eval(f'({inner_expr})', {"__builtins__": safe_builtins}, self.eval_env)
                    self.eval_env[vname] = val
                    resolved_any = True
                except:
                    pass
            
            if not resolved_any:
                break

        self._resolve_rotmat_vector_offsets(safe_builtins)
    
    def _resolve_rotmat_vector_offsets(self, safe_builtins: dict) -> None:
        """Resolve `post_off_X = rot_mat @ mathutils.Vector((...))` even when the
        Vector(...) call spans multiple lines (scalar_pattern only sees single-line
        assignments).  Without this, `bench_cx + post_off_L.x` evaluates to garbage
        and island posts collapse to the origin in describe / Stage 8."""
        if "rot_mat" not in self.eval_env or "mathutils" not in self.eval_env:
            return
        for m in re.finditer(
            r"^ {4}(\w+)\s*=\s*rot_mat\s*@\s*mathutils\.Vector",
            self.code,
            re.MULTILINE,
        ):
            vname = m.group(1)
            if vname in self.eval_env:
                continue
            tail = self.code[m.end() :]
            p0 = tail.find("(")
            if p0 < 0:
                continue
            abs_open = m.end() + p0
            vec_call = self._extract_balanced_paren(self.code, abs_open)
            if not vec_call or len(vec_call) < 2:
                continue
            inner = vec_call[1:-1].strip()
            if inner.startswith("(") and inner.endswith(")"):
                inner = inner[1:-1].strip()
            inner = self._strip_trailing_comment_outside_strings(inner).strip()
            try:
                tup = eval(
                    f"({inner})",
                    {"__builtins__": safe_builtins},
                    self.eval_env,
                )
                vec = self.eval_env["mathutils"].Vector(tup)
                self.eval_env[vname] = self.eval_env["rot_mat"] @ vec
            except Exception:
                pass
    
