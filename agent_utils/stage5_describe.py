    def _find_code_match(self, candidate_name: str, allow_loose: bool = True) -> Optional[str]:
        if not candidate_name:
            return None
        cand_norm = self._normalize_match_text(candidate_name)
        cand_tokens = set(cand_norm.split())
        if not cand_tokens:
            return None

        best_name = None
        best_score = 0.0
        for obj in self.objects:
            code_norm = self._normalize_match_text(obj.name)
            code_tokens = set(code_norm.split())
            if not code_tokens:
                continue
            if cand_norm == code_norm:
                return obj.name
            overlap = len(cand_tokens & code_tokens)
            score = overlap / max(len(cand_tokens), len(code_tokens))
            if cand_tokens.issubset(code_tokens) or code_tokens.issubset(cand_tokens):
                score = max(score, 0.75)
            if score > best_score:
                best_score = score
                best_name = obj.name

        threshold = 0.45 if allow_loose else 0.7
        return best_name if best_score >= threshold else None

    def _build_orphan_inventory(self):
        """Mark Stage1/2 inventory objects that have no Stage3/4 code object."""
        if not self.inventory_objects:
            self.orphan_objects = []
            return

        id_to_item = {
            item.get("source_object_id"): item
            for item in self.inventory_objects
            if item.get("source_object_id")
        }
        orphans = []

        for item in self.inventory_objects:
            name = item.get("name") or ""
            placement = item.get("placement_type")
            category = item.get("category")
            allow_loose = category == "major" or placement in {"floor", "wall"}
            code_match = self._find_code_match(name, allow_loose=allow_loose)

            parent_name = item.get("parent_name")
            parent_code_match = self._find_code_match(parent_name, allow_loose=True)
            if not parent_code_match:
                parent_id = item.get("parent_object")
                parent_item = id_to_item.get(parent_id)
                if parent_item:
                    parent_code_match = parent_item.get("code_match_name")

            item["represented_in_code"] = bool(code_match)
            item["code_match_name"] = code_match
            item["parent_code_match_name"] = parent_code_match

            if code_match:
                continue

            if placement == "surface":
                reason = "listed as a surface object in Stage 1/2 but absent from Stage 3/4 bbox code"
            elif placement == "wall":
                reason = "listed as a wall object in Stage 1 but absent from Stage 3/4 bbox code"
            elif placement == "floor":
                reason = "listed as a floor object in Stage 1/2 but absent from Stage 3/4 bbox code"
            else:
                reason = "listed in Stage 1/2 but absent from Stage 3/4 bbox code"

            orphan = dict(item)
            orphan["orphan_reason"] = reason
            orphan.setdefault("object_type", "")
            orphan.setdefault("appearance", "")
            orphan.setdefault("material_description", "")
            orphan.setdefault("color_description", "")
            orphan.setdefault("description", "")
            orphans.append(orphan)

        self.orphan_objects = orphans
        self._log(
            f"Inventory reconciliation: {len(orphans)} orphan objects",
            "warning" if orphans else "success",
        )

    def _fallback_describe_orphan(self, orphan: Dict[str, Any]) -> Dict[str, Any]:
        name = orphan.get("name") or "Object"
        parent = orphan.get("parent_name")
        placement = orphan.get("placement_type") or "unknown"
        base = f"{name} listed by Stage 1/2 as a {placement} object"
        if parent:
            base += f" associated with {parent}"
        base += ", but not instantiated in the Stage 3/4 Blender code."
        orphan["object_type"] = orphan.get("object_type") or name.lower()
        orphan["appearance"] = orphan.get("appearance") or "Visible in the source image inventory, exact geometry not present in code."
        orphan["material_description"] = orphan.get("material_description") or "unspecified material"
        orphan["color_description"] = orphan.get("color_description") or "image-dependent color"
        orphan["description"] = orphan.get("description") or base
        return orphan

