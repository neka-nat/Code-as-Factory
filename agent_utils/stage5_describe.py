    def _parse_materials(self):
        """Parse material definitions"""
        pattern = r'(\w+)\s*=\s*create_material\s*\(\s*["\']([^"\']+)["\']\s*,\s*\(([^)]+)\)'
        for match in re.finditer(pattern, self.code):
            var_name = match.group(1)
            mat_name = match.group(2)
            color_str = match.group(3)
            try:
                color_values = [float(x.strip()) for x in color_str.split(',')]
                self.materials[var_name] = {
                    'name': mat_name,
                    'color': color_values[:3] if len(color_values) >= 3 else [0.5, 0.5, 0.5]
                }
            except:
                self.materials[var_name] = {'name': mat_name, 'color': [0.5, 0.5, 0.5]}
    
