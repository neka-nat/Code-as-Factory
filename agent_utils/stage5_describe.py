    @staticmethod
    def _collect_full_call(lines: List[str], start_idx: int) -> Tuple[str, int]:
        """Collect a full (possibly multi-line) function call starting at start_idx.
        Returns (full_call_str_stripped, end_idx)."""
        paren_depth = 0
        collected = []
        for j in range(start_idx, len(lines)):
            text = lines[j]
            cleaned = BlenderCodeParser._strip_trailing_comment_outside_strings(
                text.strip()
            )
            collected.append(cleaned)
            paren_depth += text.count('(') - text.count(')')
            if paren_depth <= 0:
                return ' '.join(collected), j
        return ' '.join(collected), len(lines) - 1
    
    def _parse_all_objects(self):
        """Parse ALL object creation calls including those in for-loops."""
        lines = self.code.split('\n')
        i = 0
        while i < len(lines):
            line = lines[i].strip()
            
            # Detect for-loop that contains create_box / create_cylinder
            if line.startswith('for ') and line.endswith(':'):
                loop_header = line
                loop_body_lines = []
                i += 1
                if i < len(lines):
                    base_indent = len(lines[i]) - len(lines[i].lstrip()) if lines[i].strip() else 0
                    while i < len(lines) and (not lines[i].strip() or (len(lines[i]) - len(lines[i].lstrip()) >= base_indent and lines[i].strip())):
                        loop_body_lines.append(lines[i])
                        i += 1
                        if i >= len(lines):
                            break
                
                body_text = '\n'.join(loop_body_lines)
                # Direct primitive creators — fast path.
                creates_directly = (
                    'create_box' in body_text
                    or 'create_cylinder' in body_text
                    or 'create_detailed_object' in body_text
                )
                # Indirect creators — the body calls a top-level helper
                # (e.g. ``add_chair``, ``make_dining_set``) whose body in
                # turn calls ``create_box``. Detect by name match against
                # ``_collect_helper_funcs()`` whose own body contains
                # primitive creators. Without this, ``for ... in
                # chair_positions.items(): add_chair(...)`` would silently
                # skip the loop and lose every chair.
                creates_via_helper = False
                if not creates_directly:
                    creators = self._helper_func_creator_names()
                    if creators:
                        import re as _re
                        for fn_name in creators:
                            if _re.search(rf"\b{_re.escape(fn_name)}\s*\(", body_text):
                                creates_via_helper = True
                                break
                if creates_directly or creates_via_helper:
                    self._unroll_loop(loop_header, loop_body_lines)
                continue
            
            # Direct create_box / create_cylinder calls (handle multi-line)
            if 'create_box(' in line or 'create_cylinder(' in line:
                full_call, end_idx = self._collect_full_call(lines, i)
                obj = self._parse_single_call(full_call)
                if obj and not self._is_architectural(obj.name):
                    self.objects.append(obj)
                i = end_idx
            
            i += 1
    
    def _parse_single_call(self, line: str) -> Optional[ObjectInfo]:
        """Parse a single create_box/create_cylinder call from a line."""
        line = line.strip()
        
        # Determine shape
        if 'create_box(' in line:
            shape = 'box'
            func_start = line.index('create_box(') + len('create_box(')
        elif 'create_cylinder(' in line:
            shape = 'cylinder'
            func_start = line.index('create_cylinder(') + len('create_cylinder(')
        else:
            return None
        
        # Extract all arguments inside the outer parentheses
        # Find the opening paren of the function call
        call_start = line.index('create_box(') if shape == 'box' else line.index('create_cylinder(')
        paren_pos = line.index('(', call_start)
        full_args_str = self._extract_balanced_paren(line, paren_pos)
        if not full_args_str:
            return None
        
        # Strip outer parens to get inner content
        inner = full_args_str[1:-1].strip()
        
        # Split top-level arguments
        args = self._split_top_level_args(inner)
        if len(args) < 3:
            return None
        
        # arg[0] = name (string), arg[1] = location (tuple), arg[2] = dimensions (tuple)
        name = self._extract_string_value(args[0])
        if not name:
            return None
        
        location = self._safe_eval_tuple(args[1])
        dimensions = self._safe_eval_tuple(args[2])
        
        # Parse rotation/material/collection. Stage 3/4 code may pass these
        # either as keywords or as positional args:
        # create_box(name, loc, dims, bench_rot, mat_bench, coll_zone)
        rotation = (0.0, 0.0, 0.0)
        material_name = ""
        collection_name = "Scene"
        
        for idx, arg in enumerate(args[3:], start=3):
            arg = arg.strip()
            if arg.startswith('rotation=') or arg.startswith('rotation ='):
                rot_expr = arg.split('=', 1)[1].strip()
                rotation = self._safe_eval_tuple(rot_expr)
            elif arg.startswith('material=') or arg.startswith('material ='):
                mat_var = arg.split('=', 1)[1].strip()
                if mat_var in self.materials:
                    material_name = self.materials[mat_var]['name']
            elif arg.startswith('collection=') or arg.startswith('collection ='):
                col_var = arg.split('=', 1)[1].strip()
                if col_var in self.collections:
                    collection_name = self.collections[col_var]
            elif '=' not in arg:
                if idx == 3:
                    rotation = self._safe_eval_tuple(arg)
                elif idx == 4:
                    if arg in self.materials:
                        material_name = self.materials[arg]['name']
                elif idx == 5:
                    if arg in self.collections:
                        collection_name = self.collections[arg]
        
        import math
        rotation_degrees = tuple(round(r * 180 / math.pi, 1) for r in rotation)
        
        return ObjectInfo(
            name=name,
            shape=shape,
            location=location,
            dimensions=dimensions,
            rotation=rotation,
            rotation_degrees=rotation_degrees,
            material_name=material_name,
            collection=collection_name
        )
    
    def _collect_literal_iterables(self) -> Dict[str, Any]:
        """Walk the full AST and collect every ``name = <iterable>`` assignment
        as a potential iterable source for ``_unroll_loop``.

        We walk the whole tree (not just ``tree.body``) because real Stage 3/4
        scripts wrap everything in ``def run_layout_engine(): ...``, so the
        interesting assignments live inside a ``FunctionDef.body``.

        Two-pass strategy:

        **Pass 1** — ``ast.literal_eval`` on every Assign. Captures pure
        list / dict / tuple / set literals.  Cheap, safe, original behaviour.

        **Pass 2** — Helper-call rescue.  Some Stage 3/4 scripts emit::

            chair_positions = {
                "obj_002_Dining_Chair_North_Left": norm_to_world(0.46, 0.40, SCENE_W, SCENE_D),
                ...
            }

        The values are helper-function calls, not literal tuples, so
        ``ast.literal_eval`` rejects the **whole** dict and we lose every
        entry — including the chair names that downstream stages need.
        For each non-literal Assign whose RHS contains an ``ast.Call``,
        we re-run ``eval`` against a sandbox containing:

        - Pass-1 literals (so ``SCENE_W`` / ``table_dim`` resolve)
        - Top-level ``def`` helpers via ``_collect_helper_funcs`` (so
          ``norm_to_world`` / ``yaw_to_face`` etc. resolve)
        - ``math`` and ``__builtins__``

        Only iterable results (list / dict / tuple / set) are accepted, so
        ``mat_x = create_pbr_material(...)`` (which returns a Material or
        sentinel) does NOT poison ``out``.  Newly captured iterables are
        written back into the sandbox so chained references like
        ``positions = build_grid(SCENE_W); subset = positions[:4]`` resolve.

        Last-write-wins for duplicate names (matches Python semantics for
        the outermost surrounding scope, which is what loops typically
        iterate).
        """
        import math as _math

        out: Dict[str, Any] = {}
        try:
            tree = ast.parse(self.code)
        except SyntaxError:
            return out

        assigns: List[Tuple[str, ast.AST]] = []
        all_literals: Dict[str, Any] = {}  # scalars + iterables, for sandbox
        for node in ast.walk(tree):
            if not isinstance(node, ast.Assign):
                continue
            if len(node.targets) != 1 or not isinstance(node.targets[0], ast.Name):
                continue
            name = node.targets[0].id
            assigns.append((name, node.value))
            try:
                val = ast.literal_eval(node.value)
            except (ValueError, SyntaxError, TypeError):
                continue
            all_literals[name] = val
            if isinstance(val, (list, dict, tuple, set)):
                out[name] = val

        sandbox: Dict[str, Any] = dict(self.eval_env)
        sandbox.setdefault("math", _math)
        sandbox.setdefault("__builtins__", __builtins__)
        # Sandbox needs scalar literals too (SCENE_W=8.0, table_x=2.5, ...)
        # so helper-call args resolve. ``out`` keeps only iterables because
        # downstream ``_unroll_loop`` only iterates over collections.
        sandbox.update(all_literals)
        try:
            sandbox.update(self._collect_helper_funcs())
        except Exception:
            pass

        # Pass 2 a) Single-target Assign: rescue helper-call results (both
        # scalar and iterable) so a follow-up Assign that depends on them
        # (e.g. ``chair_positions = {k: norm_to_world(0.5, 0.4, SCENE_W,
        # SCENE_D)}``) can resolve. Only iterable results land in ``out``;
        # scalar results stay in the sandbox for downstream eval and for
        # ``_unroll_loop`` (see ``self._eval_sandbox`` cache below).
        # Pass 2 b) Tuple-target Assign: ``table_x, table_y = norm_to_world(...)``
        # is the canonical pattern that fed scalar table coordinates into
        # ``for ... in chair_positions.items(): ... face_target_rotation(pos,
        # (table_x, table_y))``. Without unpacking these, the chair loop
        # NameErrors on table_x even after ``chair_positions`` is rescued.
        for stmt_node in ast.walk(tree):
            if not isinstance(stmt_node, ast.Assign):
                continue
            if len(stmt_node.targets) != 1:
                continue
            target = stmt_node.targets[0]
            rhs = stmt_node.value
            if not any(isinstance(n, ast.Call) for n in ast.walk(rhs)):
                continue
            try:
                val = eval(ast.unparse(rhs), sandbox)
            except Exception:
                continue
            if isinstance(target, ast.Name):
                sandbox[target.id] = val
                if target.id not in out and isinstance(val, (list, dict, tuple, set)):
                    out[target.id] = val
            elif isinstance(target, (ast.Tuple, ast.List)):
                if not isinstance(val, (tuple, list)):
                    continue
                if len(target.elts) != len(val):
                    continue
                for elt, v in zip(target.elts, val):
                    if isinstance(elt, ast.Name):
                        sandbox[elt.id] = v

        self._eval_sandbox = sandbox
        # Sync scalar / iterable values into the helper sandbox so any
        # helper whose body references outer-scope vars (e.g. ``SCENE_W``,
        # ``table_x``) resolves them via ``__globals__`` at call time.
        # ``setdefault`` preserves already-installed helpers and stubs.
        helper_sb = getattr(self, "_helper_funcs_sandbox", None)
        if helper_sb is not None:
            for k, v in sandbox.items():
                if k in ("__builtins__",):
                    continue
                helper_sb.setdefault(k, v)
        return out

    def _helper_func_creator_names(self) -> List[str]:
        """Return names of top-level helper ``def``s whose body contains a
        primitive creator call (``create_box`` / ``create_cylinder`` /
        ``create_detailed_object``).

        These are the helpers that *indirectly* create objects when called
        — e.g. ``add_chair``, ``make_dining_chair``, ``spawn_armchair``.
        Used by ``_parse_all_objects`` so a loop like ``for ... in
        chair_positions.items(): add_chair(...)`` is recognised as an
        object-creating loop and dispatched to ``_unroll_loop``.

        Cached on the instance — the AST walk is non-trivial.
        """
        cached = getattr(self, "_helper_creator_cache", None)
        if cached is not None:
            return cached
        names: List[str] = []
        try:
            tree = ast.parse(self.code)
        except SyntaxError:
            self._helper_creator_cache = names
            return names
        primitives = {"create_box", "create_cylinder", "create_detailed_object"}
        for node in tree.body:
            if not isinstance(node, ast.FunctionDef):
                continue
            for sub in ast.walk(node):
                if (
                    isinstance(sub, ast.Call)
                    and isinstance(sub.func, ast.Name)
                    and sub.func.id in primitives
                ):
                    names.append(node.name)
                    break
        # Don't list the primitives themselves — they're handled by the
        # fast-path direct check.
        names = [n for n in names if n not in primitives]
        self._helper_creator_cache = names
        return names

    def _collect_helper_funcs(self) -> Dict[str, Any]:
        """Inject scene-level ``def`` helpers (e.g. ``yaw_to_face`` /
        ``to_world`` / ``add_chair``) into ``loop_env`` so loop bodies can
        call them without NameError when we ``exec`` them in the unroll
        sandbox.

        Each helper is exec'd in its own try/except — one bad helper (e.g.
        one that touches ``bpy`` at definition time) does not poison the rest.

        Critically, helper bodies that call ``create_box`` /
        ``create_cylinder`` (e.g. ``add_chair`` → 3× ``create_box``) bind
        those names through their own ``__globals__`` at *call* time, NOT
        at def time. So:

        1. We pre-stub ``create_box`` / ``create_cylinder`` /
           ``create_detailed_object`` in the shared ``sandbox_globals``
           dict BEFORE exec'ing helper defs, then exec all helpers into
           that same dict so each helper's ``__globals__`` references it.
        2. We cache the dict on ``self._helper_funcs_sandbox`` so
           ``_unroll_loop`` can later overwrite the stubs with its
           collector functions; helper invocations during loop simulation
           will then see the collectors via ``LOAD_GLOBAL`` at call time.

        The result is cached on ``self._helper_funcs_cache`` — re-running
        the AST walk + exec for every loop is unnecessarily slow.
        """
        cached = getattr(self, "_helper_funcs_cache", None)
        if cached is not None:
            return cached

        import math as _math
        helpers: Dict[str, Any] = {}
        try:
            tree = ast.parse(self.code)
        except SyntaxError:
            self._helper_funcs_cache = helpers
            return helpers

        src_lines = self.code.split('\n')
        sandbox_globals: Dict[str, Any] = dict(self.eval_env)
        sandbox_globals.setdefault("math", _math)
        sandbox_globals.setdefault("__builtins__", __builtins__)
        # Pre-stub primitive creators so any helper def that closes over
        # ``create_box`` / ``create_cylinder`` resolves to *this* dict
        # entry; ``_unroll_loop`` can then overwrite the stubs with
        # collector functions and helper invocations will see them.
        sandbox_globals.setdefault("create_box", lambda *a, **kw: None)
        sandbox_globals.setdefault("create_cylinder", lambda *a, **kw: None)
        sandbox_globals.setdefault("create_detailed_object", lambda *a, **kw: None)

        for node in tree.body:
            if not isinstance(node, ast.FunctionDef):
                continue
            end = getattr(node, "end_lineno", None)
            if end is None:
                continue
            chunk = "\n".join(src_lines[node.lineno - 1 : end])
            try:
                exec(chunk, sandbox_globals)
                fn = sandbox_globals.get(node.name)
                if callable(fn):
                    helpers[node.name] = fn
            except Exception:
                continue

        # Don't return the primitives themselves — they're stubs, not real
        # creators, and ``_unroll_loop`` installs its own collector for
        # the local LOAD_NAME path. Helper bodies still see them via
        # ``__globals__`` (which is sandbox_globals).
        for primitive in ("create_box", "create_cylinder", "create_detailed_object"):
            helpers.pop(primitive, None)

        self._helper_funcs_sandbox = sandbox_globals
        self._helper_funcs_cache = helpers
        return helpers

    def _unroll_loop(self, header: str, body_lines: List[str]):
        """Unroll a for-loop to extract all objects it creates."""
        import math
        
        # Parse loop: for i, xxx in enumerate(data): or for name, x, y, h in data: etc.
        # Strategy: build a safe eval environment, then simulate the loop
        
        body_text = '\n'.join(l.strip() for l in body_lines if l.strip())
        
        # Extract the iterable and loop variable(s)
        # Common patterns:
        #   for i, off in enumerate(chair_offsets):
        #   for i, (xn, yn, rot) in enumerate(armchair_data):
        #   for i, yn in enumerate(reception_chairs):
        #   for name, xn, yn, h in plants:
        #   for name, (x, y) in chair_positions.items():     ← dict literal
        
        # AST-based collector (replaces an old list-only regex). This now
        # captures dict / tuple / set literals AND helper-call results
        # (e.g. ``chair_positions = {k: norm_to_world(...)}``), and as a
        # side-effect populates ``self._eval_sandbox`` with every Assign we
        # could resolve — scalars (``SCENE_W=8.0``, ``table_x=...``),
        # iterables, and helper functions all live there.
        list_vars = self._collect_literal_iterables()
        
        # Try to simulate the loop
        try:
            # Use the full eval sandbox if available — it already contains
            # ``list_vars`` + helpers + scalar literals + scalar helper-call
            # results. This lets a chair-loop body that calls
            # ``face_target_rotation(pos, (table_x, table_y))`` resolve
            # ``table_x`` (which came from a tuple unpack of norm_to_world).
            loop_env = dict(self.eval_env)
            full_sandbox = getattr(self, "_eval_sandbox", None)
            if full_sandbox is not None:
                loop_env.update(full_sandbox)
            else:
                loop_env.update(list_vars)
                loop_env.update(self._collect_helper_funcs())
            loop_env['enumerate'] = enumerate
            loop_env['range'] = range
            
            # Execute the loop header + body, but replace create_box/create_cylinder
            # with a collector function
            collected = []
            
            def fake_create_box(name, loc, dims, rotation=(0, 0, 0), material=None, collection=None, show_direction=True):
                mat_name = ""
                for k, v in self.materials.items():
                    # material parameter in loop is a variable reference
                    pass
                collected.append(('box', name, loc, dims, rotation, material, collection))
            
            def fake_create_cylinder(name, loc, dims, rotation=(0, 0, 0), material=None, collection=None):
                collected.append(('cylinder', name, loc, dims, rotation, material, collection))
            
            loop_env['create_box'] = fake_create_box
            loop_env['create_cylinder'] = fake_create_cylinder
            # Critical for indirect creators: helpers like ``add_chair``
            # that internally call ``create_box`` resolve those names via
            # their own ``__globals__`` at call time, NOT via our
            # ``loop_env`` locals. We override the primitive stubs in the
            # shared helper sandbox so a ``add_chair(...)`` invocation
            # collects rather than NameErrors on ``bpy``.
            helper_sb = getattr(self, "_helper_funcs_sandbox", None)
            if helper_sb is not None:
                helper_sb['create_box'] = fake_create_box
                helper_sb['create_cylinder'] = fake_create_cylinder
                helper_sb['create_detailed_object'] = fake_create_box
            
            # Also put materials as their variable names pointing to their string names
            # so we can resolve them later
            mat_var_to_name = {}
            for var, info in self.materials.items():
                loop_env[var] = var  # pass the variable name as the value
                mat_var_to_name[var] = info['name']
            
            # Put collections as their variable names
            col_var_to_name = {}
            for var, cname in self.collections.items():
                loop_env[var] = var
                col_var_to_name[var] = cname
            
            # Build executable code
            loop_code = header + '\n'
            for bl in body_lines:
                # Only include lines that are part of the loop body
                if bl.strip():
                    loop_code += bl + '\n'
            
            # NOTE: scene helpers (yaw_to_face / to_world / …) are now in
            # ``loop_env`` so they're resolvable as locals during exec.
            exec(loop_code, {"__builtins__": {
                "range": range, "enumerate": enumerate, "len": len,
                "abs": abs, "min": min, "max": max, "round": round,
                "int": int, "float": float, "str": str,
                "tuple": tuple, "list": list, "dict": dict, "set": set,
                "sorted": sorted, "reversed": reversed,
            }}, loop_env)
            
            # Convert collected objects
            for shape, name, loc, dims, mat_ref, col_ref, *_ in collected:
                # Handle extra args if collected order varies
                if len(collected[0]) == 7:
                    shape, name, loc, dims, rot, mat_ref, col_ref = collected[0]
                
                # Resolve names
                try:
                    loc_tuple = tuple(float(v) for v in loc)
                except:
                    loc_tuple = (0.0, 0.0, 0.0)
                try:
                    dim_tuple = tuple(float(v) for v in dims)
                except:
                    dim_tuple = (1.0, 1.0, 1.0)
                
                break  # Just use collected directly below
            
            # Process all collected objects
            for item in collected:
                shape, name, loc, dims, rot, mat_ref, col_ref = item
                
                try:
                    loc_tuple = tuple(float(v) for v in loc)
                except:
                    loc_tuple = (0.0, 0.0, 0.0)
                try:
                    dim_tuple = tuple(float(v) for v in dims)
                except:
                    dim_tuple = (1.0, 1.0, 1.0)
                try:
                    rot_tuple = tuple(float(v) for v in rot) if rot else (0.0, 0.0, 0.0)
                except:
                    rot_tuple = (0.0, 0.0, 0.0)
                
                material_name = mat_var_to_name.get(mat_ref, "") if isinstance(mat_ref, str) else ""
                collection_name = col_var_to_name.get(col_ref, "Scene") if isinstance(col_ref, str) else "Scene"
                
                rotation_degrees = tuple(round(r * 180 / math.pi, 1) for r in rot_tuple)
                
                obj = ObjectInfo(
                    name=str(name),
                    shape=shape,
                    location=loc_tuple,
                    dimensions=dim_tuple,
                    rotation=rot_tuple,
                    rotation_degrees=rotation_degrees,
                    material_name=material_name,
                    collection=collection_name
                )
                
                if not self._is_architectural(obj.name):
                    self.objects.append(obj)
        
        except Exception as e:
            # Loud warning so this doesn't silently swallow whole groups of
            # objects (the dining-chair-loop bug stayed hidden for a while
            # exactly because this path was silent).
            print(
                f"⚠️  stage5_describe._unroll_loop failed for "
                f"header={header!r}: {type(e).__name__}: {e}",
                file=sys.stderr,
            )
            # Fallback: try to at least extract static-name objects from body.
            # We collect across multi-line calls so a multi-line create_box(...)
            # inside the loop body doesn't get cut off mid-call.
            j = 0
            while j < len(body_lines):
                bl_stripped = body_lines[j].strip()
                if 'create_box(' in bl_stripped or 'create_cylinder(' in bl_stripped:
                    full_call, end_idx = self._collect_full_call(body_lines, j)
                    obj = self._parse_single_call(full_call)
                    if obj and not self._is_architectural(obj.name):
                        self.objects.append(obj)
                    j = end_idx + 1
                    continue
                j += 1
    
